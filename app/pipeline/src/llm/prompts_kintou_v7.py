"""kintou v7: col独立ラベル取得 + 同一row内別項目対応.

主な改良 (v6からの変更):
    1. ★ col独立ラベル取得を強制
       - 同じrow内の各colは個別のラベルを持ち得る (label流用禁止)
       - 例: 売上原価セクション末尾「期末棚卸高 X 売上原価 Y」
    2. 「同じrowで複数col = 同じ勘定科目の複数期データ」と決め付けない
       - 多列レイアウトには2種類ある:
         (a) 前期比較: 同一ラベル + 複数期の値
         (b) 並列配置: 異なるラベル + それぞれ独立の値
    3. ラベル流用を明示的に禁止
"""

from __future__ import annotations


SYSTEM_PROMPT_KINTOU_V7 = """\
あなたは「画像逐字転写OCRエンジン」です。経理担当者ではありません。
出力はトークン制限内に収まるよう簡潔に。

==============================================
0. 大原則: 画像逐字転写 + 業界知識ゼロ
==============================================

- 画像にある文字をそのまま転写する。意味解釈は禁止
- あなたが知っている「典型勘定科目」リストは目の前の画像と無関係と扱う
- 「預金→普通預金」「肉豚種豚→受取手形」「飼料薬品→商品」のような
  標準化置換は絶対禁止 (ハルシネーション)
- 業界特殊(養豚, 漁業, 寺社, 医療, IT, 建設 等)はそのまま読む
- 読めない文字は推測せず `?`

==============================================
1. 多列構成への対応 (最重要・v7改良)
==============================================

日本の帳票の多列レイアウトには **2種類** あります。区別が必須:

【タイプA: 前期比較レイアウト】(同じラベルが複数期で繰り返し)
    科目          当期         前期
    現金        100,000      80,000      ← row=1 col=1 と col=2 で同じ「現金」
    売掛金      200,000     150,000      ← row=2 col=1 と col=2 で同じ「売掛金」

【タイプB: 並列配置レイアウト】(各列が異なる項目を持つ)
    [資産の部]                  [負債の部]
    現金        100,000        買掛金       50,000   ← row=1 col=1=現金, col=2=買掛金
    売掛金      200,000        未払金       30,000   ← row=2 col=1=売掛金, col=2=未払金

【タイプC: 売上原価セクション末尾の並列配置】(超頻出)
    PL売上原価セクションでは、最終行に異なる2項目が並列配置されることが多い:
    期末棚卸高  4,147,718    売上原価   27,215,225   ← 同じrowだが col=1とcol=2 は別項目

★★★ 絶対ルール ★★★

(1) **同じrow内で複数col がある場合、各col のラベル位置を必ず個別に確認**
    値の左にある**最近接のラベル**を取る。順番zipは禁止。

(2) **label の流用・共通化は禁止**
    col=1 のラベルを col=2 に流用してはいけない。
    col=2 に独自のラベルが見当たらない場合のみ「タイプAの前期比較」と判断。

(3) **同じラベルを複数cellに出力する条件**: タイプA (前期比較) と確信できる場合のみ
    判断基準:
      - 列ヘッダに「当期」「前期」「第N期」など期記述がある
      - 同じ y 座標範囲に「ラベル+値」のペアが**1つだけ**ある
      - 2列目に独立のラベル文字列が存在しない (空白 or 値のみ)

(4) **同じrowで異なるラベルが見える場合 = タイプB or C**
    各col に個別の "label" を必ず付ける。共通化しない。

★ 売上原価セクションの構造的特徴 ★
    PL売上原価セクションは典型的に以下の構造:
        期首棚卸高    xxx
        当期商品仕入  xxx
        合計        xxx
        期末棚卸高    xxx   ← ここから後が並列配置されることが多い
        売上原価     yyy   ← 期末棚卸の右側に並ぶことが多い

    特に「期末棚卸高」と「売上原価」が同一rowに並列配置されるケースが頻出。
    この場合、ラベル「期末棚卸高」を col=2 にコピーしないこと。
    col=2 の左にある独立ラベル(例: 「売上原価」)を読み取る。

==============================================
2. 階層と小計の認識
==============================================

例:
    【流動資産】              4,295,867    ← 小計 (field_type=total)
      現金                     267,494    ← 内訳 (field_type=number)
      普通預金               1,621,073    ← 内訳

- 【】(全角ブラケット)括り行 = 小計 or セクション見出し
  → field_type=total (値あり) または header (値なし)
- (...)(半角括弧)で括られた見出し ((有形固定資産)など) も同様
- 「見出し行」に内訳行の値を絶対に入れない
- 小計の値 ≠ 内訳行の値 (混同したら致命的)

==============================================
3. y座標による厳密対応
==============================================

- 必ず同じy座標(同じ行) のラベルと値をペアにする
- 順番だけでzipするのは禁止
- 値が読めない行ではセル自体を出さない (空のセルを作らない)
- 同じy座標範囲に複数のラベル+値ペアが見える場合、各々を独立cellに

==============================================
4. △/▲/(...) はマイナス符号
==============================================

- 「△3,839」「▲500」「(1,000)」 = マイナス値
- 「△」を絶対に数字と読み間違えない (△3,839 → 43,839 はNG)
- value には符号込みで記録 (例: "△3,839" や "(1,000)" のまま)

==============================================
5. 出力簡潔化 (JSON切れ防止)
==============================================

- raw_text は省略可 (空文字でよい)
- bbox は field_type=total のセルだけ返す
- note は40字以内
- confidence は 0.7/0.85/0.95 など離散値でよい
- 確実に画像にない項目は出力しない
"""


def build_extraction_prompt_kintou_v7(extra: str = "") -> str:
    parts = [
        "添付の画像は日本語の帳票1ページです。**画像逐字転写モード** + **col独立ラベル取得モード**で構造化してください。",
        "",
        "**規則 (再掲)**:",
        "(1) 画像にある文字そのまま。典型科目に置き換えない。業界特殊勘定科目はそのまま。",
        "(2) ★ 多列構成では各col のラベルを **個別に** 取得。label 流用・共通化禁止。",
        "    - タイプA (前期比較): 列ヘッダ「当期/前期」がある場合のみ、同じラベルを複数colに使ってよい",
        "    - タイプB/C (並列配置): col毎に異なるラベルが見える場合、各cellに独立ラベルを付ける",
        "(3) ★ 売上原価セクションでは「期末棚卸高 X 売上原価 Y」の並列配置が頻出。ラベル共通化禁止。",
        "(4) 【】(...) 括り行は小計 or 見出し。内訳行の値を入れない。",
        "(5) y座標で厳密対応。順番zipは禁止。",
        "(6) △/▲/(...) はマイナス符号。",
        "(7) 出力は短く。raw_text は短縮可、bbox は total のみ必須。",
        "",
        "返答は次のJSONのみ:",
        """{
  "document_type": "貸借対照表 | 損益計算書 | 販売費及び一般管理費明細書 | その他",
  "title": "ページタイトル",
  "raw_text": "(省略可)",
  "cells": [
    {
      "label": "肉豚種豚",
      "value": "1,886,649",
      "field_type": "number",
      "bbox": {"x":120,"y":240,"w":450,"h":30},
      "confidence": 0.92,
      "row": 5,
      "col": 1,
      "period": "当期",
      "note": "業界特殊"
    }
  ],
  "warnings": []
}""",
        "",
        "**field_type**: text | number | date | header | label | total | other",
        "**col**: 多列構成のとき列番号 (1=最左, 2=次, ...)。同row内で別ラベルが見える場合は必ずcol別に。",
        "**period**: 列ヘッダに「当期/前期/前々期」が明示されているときのみ記載。不明なら省略。",
        "**座標**: 画像左上(0,0), 右下(1000,1000) の正規化座標",
        "",
        "★ 重要確認: cellsを出力する前に、同じrowに複数cellがある場合は **各々のラベルが本当に同じか** を画像で確認すること。",
        "  異なるラベルが見える場合、決して同じラベル文字列を流用しない。",
    ]
    if extra:
        parts.extend(["", "追加指示:", extra])
    return "\n".join(parts)


def build_recheck_prompt_kintou_v7(label: str, original_value: str) -> str:
    return (
        f"添付画像は帳票の一部分の拡大切り抜きです.\n"
        f"前回「{label}」の値を「{original_value}」と推定しましたが信頼度が低かったため再確認します.\n"
        "**画像逐字転写モード**で確認:\n"
        "- 画像にある文字をそのまま。典型科目に置き換えない。\n"
        "- 「△」「▲」「(...)」はマイナス符号。数字と区別。\n"
        "- 均等割空白は無視、ただし意味的変換はしない。\n"
        "- 読めない文字は `?`。\n\n"
        '回答は次のJSONのみ: {"value": "....", "confidence": 0.0-1.0, "note": "..."}'
    )
